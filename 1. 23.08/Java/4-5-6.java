import java.util.*;

class Main {
	public static void main(String[] args)
	{
		for(int i = 0; i<=100; ++i)
		{
			System.out.printf("%d",i);
		}
		System.out.printf("\n");
		System.out.printf("\n");
		for(int i = 0; i<=100; i+=2)
		{
			System.out.printf("%d",i);
		}
		System.out.printf("\n");
		System.out.printf("\n");
		for(int i = 1; i<=99; i+=2)
		{
			System.out.printf("%d",i);
		}
	}
}
